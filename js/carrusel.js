    function imgSiguiente() {
        var img = document.getElementById("imgCarrusel");
        for (var i = 0; i < arreglo.length; i++) {
            if (arreglo[i].src == img.src) {
                if (i === arreglo.length - 1) {
                    document.getElementById("imgCarrusel").src = arreglo[0].src;
                    break;
                }
                document.getElementById("imgCarrusel").src = arreglo[i + 1].src;
                break;
            }
        }
    }

    function imgAnterior() {
        var img = document.getElementById("imgCarrusel");
        for (var i = 0; i < arreglo.length; i++) {
            if (arreglo[i].src == img.src) {
                if (i === 0) {
                    document.getElementById("imgCarrusel").src = arreglo[arreglo.length - 1].src;
                    break;
                }
                document.getElementById("imgCarrusel").src = arreglo[i - 1].src;
                break;
            }
        }
    }